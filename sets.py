days={"Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday","Sunday","Sunday","Sunday"}
print(days)

#set methods

#Adds Values to a set
days.add("Mon")
print(days)

#Removes Values To a Set
days.remove("Mon")
print(days)

#Adds multiple values
days.update([1,2,3,4,5,6,7])
print(days)

#Discards
days.discard(1)
print(days)

#Clears everything
days.clear()
print(days)